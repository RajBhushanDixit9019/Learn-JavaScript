// in this js code there is a illustration of built-in method in arrays


// creating an array 'fruits'

let fruits = ["Apple", "Banana", "Cherry", "Mango"];

// push() in array
fruits.push("Orange");
console.log(fruits);

// pop() in array

fruits.pop();
console.log(fruits);

// unshift() in array
fruits.unshift("Chiku");
console.log(fruits);

// shift() in array
fruits.shift();
console.log(fruits);